async function main() {
  console.log("🌱 Seeding Google Cloud Marketplace...\n");

  // Categories
  console.log("📁 Creating categories...");
  const categoryMap: Record<string, string> = {};
  for (const cat of CATEGORIES) {
    const c = await db.category.upsert({
      where: { slug: cat.slug },
      update: {},
      create: { ...cat },
    });
    categoryMap[cat.slug] = c.id;
  }

  // Publishers
  console.log("🏢 Creating publishers...");
  const publisherMap: Record<string, string> = {};
  for (const pub of PUBLISHERS) {
    const p = await db.publisher.upsert({
      where: { slug: pub.slug },
      update: {},
      create: { ...pub },
    });
    publisherMap[pub.slug] = p.id;
  }

  // Products
  console.log("📦 Creating products...");
  for (const prod of PRODUCTS) {
    const { pricing, categorySlug, publisherSlug, ...productData } = prod;
    const categoryId = categoryMap[categorySlug];
    const publisherId = publisherMap[publisherSlug];

    const product = await db.product.upsert({
      where: { slug: prod.slug },
      update: {},
      create: {
        ...productData,
        categoryId,
        publisherId,
      },
    });

    // Pricing plans
    const existingPlans = await db.pricingPlan.findMany({ where: { productId: product.id } });
    if (existingPlans.length === 0) {
      for (const plan of pricing) {
        await db.pricingPlan.create({
          data: {
            ...plan,
            productId: product.id,
          },
        });
      }
    }
  }

  // Seed users
  console.log("👤 Creating mock users...");
  await db.user.upsert({
    where: { email: "admin@example.com" },
    update: {},
    create: { id: "user_admin", email: "admin@example.com", name: "Admin User", role: "ADMIN" },
  });
  await db.user.upsert({
    where: { email: "buyer@example.com" },
    update: {},
    create: { id: "user_buyer", email: "buyer@example.com", name: "Jane Buyer", role: "BUYER" },
  });
  const datadogPublisher = await db.publisher.findUnique({ where: { slug: "datadog" } });
  await db.user.upsert({
    where: { email: "seller@example.com" },
    update: { publisherId: datadogPublisher?.id },
    create: { id: "user_seller", email: "seller@example.com", name: "Sam Seller", role: "SELLER", publisherId: datadogPublisher?.id },
  });

  const productCount = await db.product.count();
  const categoryCount = await db.category.count();
  const publisherCount = await db.publisher.count();

  console.log(`\n✅ Seeded successfully!`);
  console.log(`   ${categoryCount} categories`);
  console.log(`   ${publisherCount} publishers`);
  console.log(`   ${productCount} products`);
}

main()
  .catch(console.error)
  .finally(() => process.exit(0));
